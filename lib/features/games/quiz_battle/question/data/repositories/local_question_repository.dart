  @override
  Future<List<Question>> getQuestionsByCategory({
    required String categoryId,
    required String languageCode,
  }) async {
    final List<Question> questions = _questions.where((Question question) {
      return question.isActive &&
          question.categoryId == categoryId &&
          question.languageCode == languageCode;
    }).toList();

    questions.sort((Question a, Question b) {
      return a.pointValue.compareTo(b.pointValue);
    });

    return List<Question>.unmodifiable(questions);
  }

